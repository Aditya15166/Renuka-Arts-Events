// Helper functions
export const getPortfolioByCategory = (category: string): PortfolioItem[] => {
  if (category === "All") return portfolioData
  return portfolioData.filter((item) => item.category === category)
}

export const getPortfolioById = (id: number): PortfolioItem | undefined => {
  return portfolioData.find((item) => item.id === id)
}

export const getRandomPortfolioItems = (count: number): PortfolioItem[] => {
  const shuffled = [...portfolioData].sort(() => 0.5 - Math.random())
  return shuffled.slice(0, count)
}
